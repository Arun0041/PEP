// A Graph consists of a set of vertices and a set of edges. Each edge connects two vertices.
// V is a vertex and E is an edge. A graph can be represented in two ways: Adjacency Matrix and Adjacency List.


//Directed Graph: A directed graph is a graph in which the edges have a direction. The edge (u, v) is different from the edge (v, u).
//Undirected Graph: An undirected graph is a graph in which the edges do not have a direction. The edge (u, v) is the same as the edge (v, u).
//Weighted Graph: A weighted graph is a graph in which each edge has a weight. The weight of an edge can represent the cost, distance, or any other value associated with the edge.
//Unweighted Graph: An unweighted graph is a graph in which the edges do not have weights. All edges are considered to have the same weight.
//Cyclic Graph: A cyclic graph is a graph that contains at least one cycle. A cycle is a path that starts and ends at the same vertex.
//Acyclic Graph: An acyclic graph is a graph that does not contain any cycles

//Path: A sequence of vertices connected by edges. The length of a path is the number of edges in the path.

// indegree: The number of edges coming into a vertex.
// outdegree: The number of edges going out of a vertex.
